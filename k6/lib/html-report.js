function localize(value, lang) {
  if (value && typeof value === "object" && !Array.isArray(value)) {
    return value[lang] || value.en || value.tr || "";
  }
  return value;
}

function localizeList(list, lang) {
  return (list || []).map((item) => localize(item, lang));
}

function buildConfigBanner(data, options, lang) {
  const state = data.state || {};
  const meta = options.metadata || {};
  const vus = state.testRunDurationMs ? (data.metrics?.vus?.values?.max || "-") : "-";
  const duration = state.testRunDurationMs
    ? `${formatNumber(state.testRunDurationMs / 1000, 1)}s`
    : "-";
  const iterations = counterValue(data, "iterations");
  const timestamp = state.isStdOutTTY !== undefined ? new Date().toISOString().replace("T", " ").slice(0, 19) : "-";
  const val = (v) => (v === undefined || v === null || v === "" ? "-" : String(v));

  // Each item is only rendered when it has a meaningful value, so smoke tests
  // (which have no scenario name / build id) do not show empty placeholders.
  const item = (labelKey, value, escape = true) => {
    const display = value === "-" ? "-" : escape ? escapeHtml(value) : value;
    return `<div class="config-item"><span class="config-label">${tr(labelKey, lang)}</span><span class="config-val">${display}</span></div>`;
  };

  const items = [
    item("vus", vus),
    item("duration", duration),
    item("iterations", iterations),
    item("timestamp", timestamp),
  ];

  if (val(meta.baseUrl) !== "-") items.push(item("baseUrls", val(meta.baseUrl)));
  if (val(meta.environment) !== "-") items.push(item("environment", val(meta.environment)));
  if (val(meta.testType) !== "-") items.push(item("testType", val(meta.testType)));
  if (val(meta.scenarioName) !== "-") items.push(item("scenarioName", val(meta.scenarioName)));
  if (val(meta.buildId) !== "-") items.push(item("buildId", val(meta.buildId)));
  if (val(meta.commitSha) !== "-") items.push(item("commitSha", val(meta.commitSha)));

  return `<div class="config-banner">
    ${items.join("\n    ")}
  </div>`;
}

function buildExecutiveSummary(data, options, lang) {
  const verdict = options.verdict(data);
  const totalReqs = counterValue(data, "http_reqs");
  const failRate = rateValue(data, "http_req_failed");
  const failPct = failRate !== undefined ? formatPercent(failRate) : "-";
  const p95 = formatNumber(metricValue(data, "http_req_duration", "p(95)"));
  const statusLabel = verdict.ok ? "PASS" : "FAIL";

  const highFailButPass = verdict.ok && failRate > 0.5;

  const text = {
    en: highFailButPass
      ? `${totalReqs.toLocaleString()} requests. k6 HTTP fail rate: ${failPct} (expected — k6 counts 409 as failed). p95: ${p95} ms. Verdict: ${statusLabel}.`
      : `${totalReqs.toLocaleString()} requests. HTTP fail rate: ${failPct}. p95: ${p95} ms. Verdict: ${statusLabel}.`,
    tr: highFailButPass
      ? `${totalReqs.toLocaleString()} istek. k6 HTTP hata oranı: ${failPct} (beklenen — k6, 409'u failed sayar). p95: ${p95} ms. Karar: ${statusLabel}.`
      : `${totalReqs.toLocaleString()} istek. HTTP hata oranı: ${failPct}. p95: ${p95} ms. Karar: ${statusLabel}.`,
  };

  return `<div class="exec-summary ${verdict.ok ? "pass" : "fail"}">
    <span class="exec-icon">${verdict.ok ? "&#10003;" : "&#10007;"}</span>
    <span>${escapeHtml(text[lang] || text.en)}</span>
  </div>`;
}

function buildCheckRows(data, explanations, lang) {
  const checks = data.root_group?.checks || [];
  if (checks.length === 0) return `<tr><td colspan="5" class="muted">No checks.</td></tr>`;

  return checks.map((check) => {
    const passes = check.passes || 0;
    const fails = check.fails || 0;
    const total = passes + fails;
    const passRateNum = total === 0 ? 0 : (passes / total) * 100;
    const passRate = total === 0 ? "-" : `${formatNumber(passRateNum, 1)}%`;
    const ok = fails === 0;
    const barColor = ok ? "var(--green)" : "var(--red)";

    return `<tr>
      <td><span class="dot ${ok ? "good" : "bad-dot"}"></span>${escapeHtml(check.name)}</td>
      <td>
        <div class="progress-bar-wrap">
          <div class="progress-bar" style="width:${passRateNum}%;background:${barColor}"></div>
        </div>
        <strong>${passRate}</strong>
      </td>
      <td>${passes}</td>
      <td>${fails}</td>
      <td>${escapeHtml(localize(explanations[check.name], lang) || "-")}</td>
    </tr>`;
  }).join("\n");
}

function buildThresholdRows(data, explanations, lang) {
  const rows = [];

  for (const [metricName, metric] of Object.entries(data.metrics)) {
    if (!metric.thresholds) continue;

    for (const [expression, threshold] of Object.entries(metric.thresholds)) {
      rows.push(`<tr>
        <td>${escapeHtml(metricName)}</td>
        <td><code>${escapeHtml(expression)}</code></td>
        <td><span class="pill ${threshold.ok ? "pass" : "fail"}">${threshold.ok ? "PASS" : "FAIL"}</span></td>
        <td>${escapeHtml(localize(explanations[metricName], lang) || "-")}</td>
      </tr>`);
    }
  }

  return rows.length ? rows.join("\n") : `<tr><td colspan="4" class="muted">No thresholds.</td></tr>`;
}

function counterTone(name, value) {
  if (value === 0) return "zero";
  if (name === "status_500") return "danger";
  if (name === "status_other") return "warning";
  if (name === "status_201" || name === "status_200") return "success";
  if (name === "status_409") return "conflict";
  return "neutral";
}

function buildCounterCards(data, counters, lang) {
  if (!counters || counters.length === 0) {
    return `<div class="empty">No custom counters.</div>`;
  }

  return counters.map((counter) => {
    const val = counterValue(data, counter.name);
    const tone = counterTone(counter.name, val);
    return `<article class="counter-card ${tone}">
    <div class="counter-name">${escapeHtml(counter.name)}</div>
    <div class="counter-value">${val}</div>
    <p>${escapeHtml(localize(counter.explanation, lang))}</p>
  </article>`;
  }).join("\n");
}

function metricCard(label, value, explanation, tone = "neutral") {
  return `<article class="metric-card ${tone}">
    <div class="metric-label">${escapeHtml(label)}</div>
    <div class="metric-value">${escapeHtml(value)}</div>
    <p>${escapeHtml(explanation)}</p>
  </article>`;
}

