export function counterValue(data, name) {
  return data.metrics[name]?.values?.count || 0;
}

export function metricValue(data, name, key) {
  return data.metrics[name]?.values?.[key];
}

export function rateValue(data, name) {
  return data.metrics[name]?.values?.rate;
}

export function formatNumber(value, digits = 2) {
  if (value === undefined || value === null || Number.isNaN(value)) return "-";
  return Number(value).toFixed(digits);
}

export function formatPercent(value) {
  if (value === undefined || value === null || Number.isNaN(value)) return "-";
  return `${formatNumber(value * 100, 2)}%`;
}

// Format bytes (k6 reports data sent/received in bytes).
export function formatBytes(bytes) {
  if (bytes === undefined || bytes === null || Number.isNaN(bytes)) return "-";
  const units = ["B", "KB", "MB", "GB"];
  let value = Number(bytes);
  let unitIndex = 0;
  while (value >= 1024 && unitIndex < units.length - 1) {
    value /= 1024;
    unitIndex += 1;
  }
  return `${formatNumber(value, value >= 100 || unitIndex === 0 ? 0 : 2)} ${units[unitIndex]}`;
}

// Bilingual "not available" fallback for metric values that k6 did not collect.
const NA = { en: "Not available", tr: "Mevcut değil" };

function safeMetricMs(data, name, key) {
  const value = metricValue(data, name, key);
  return value === undefined || value === null ? NA : `${formatNumber(value)} ms`;
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

