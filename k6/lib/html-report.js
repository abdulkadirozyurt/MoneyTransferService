// Response time percentiles table from http_req_duration.
function buildPercentilesSection(data, lang) {
  const rows = [
    { key: "avg", label: tr("avg", lang) },
    { key: "min", label: tr("min", lang) },
    { key: "med", label: tr("med", lang) },
    { key: "p(90)", label: tr("p90", lang) },
    { key: "p(95)", label: tr("p95", lang) },
    { key: "p(99)", label: tr("p99", lang) },
    { key: "max", label: tr("max", lang) },
  ];

  const body = rows
    .map((row) => {
      const value = safeMetricMs(data, "http_req_duration", row.key);
      return `<tr><td><strong>${escapeHtml(row.label)}</strong></td><td>${escapeHtml(value)}</td></tr>`;
    })
    .join("\n");

  return `<section class="panel" id="${lang}-latency">
    <h2>${tr("percentiles", lang)}</h2>
    <p class="muted">${tr("percentileExp", lang)}</p>
    <div class="table-wrap"><table><thead><tr><th>${tr("percentile", lang)}</th><th>${tr("value", lang)}</th></tr></thead><tbody>${body}</tbody></table></div>
  </section>`;
}

// Latency breakdown: blocked / connecting / tls / sending / waiting / receiving.
function buildLatencyBreakdownSection(data, lang) {
  const metrics = [
    { name: "http_req_blocked", label: { en: "blocked", tr: "blocked" } },
    { name: "http_req_connecting", label: { en: "connecting", tr: "connecting" } },
    { name: "http_req_tls_handshaking", label: { en: "tls_handshaking", tr: "tls_handshaking" } },
    { name: "http_req_sending", label: { en: "sending", tr: "sending" } },
    { name: "http_req_waiting", label: { en: "waiting", tr: "waiting" } },
    { name: "http_req_receiving", label: { en: "receiving", tr: "receiving" } },
  ];

  const statKeys = ["avg", "med", "p(90)", "p(95)", "p(99)", "max"];

  const header = `<tr><th>${tr("metric", lang)}</th>${statKeys.map((k) => `<th>${escapeHtml(k)}</th>`).join("")}</tr>`;

  const body = metrics
    .map((m) => {
      const cells = statKeys
        .map((k) => `<td>${escapeHtml(safeMetricMs(data, m.name, k))}</td>`)
        .join("");
      return `<tr><td><code>${escapeHtml(localize(m.label, lang))}</code></td>${cells}</tr>`;
    })
    .join("\n");

  return `<section class="panel">
    <h2>${tr("latencyBreakdown", lang)}</h2>
    <p class="muted">${tr("latencyExp", lang)}</p>
    <div class="table-wrap"><table><thead>${header}</thead><tbody>${body}</tbody></table></div>
  </section>`;
}

// Throughput: average RPS, total requests, data sent / received.
// Peak RPS is intentionally NOT faked — see peakRpsNa wording.
function buildThroughputSection(data, lang) {
  const totalReqs = counterValue(data, "http_reqs");
  const rps = rateValue(data, "http_reqs");
  const dataSent = metricValue(data, "data_sent", "count");
  const dataReceived = metricValue(data, "data_received", "count");

  const rows = [
    metricCard(tr("totalRequests", lang), String(totalReqs), localize({ en: "All HTTP requests sent during this run.", tr: "Bu koşuda gönderilen tüm HTTP istekleri." }, lang)),
    metricCard(tr("avgRps", lang), rps !== undefined ? formatNumber(rps, 2) : "-", localize({ en: "Average requests per second over the whole run.", tr: "Tüm koşu boyunca saniyedeki ortalama istek." }, lang)),
    metricCard(tr("dataSent", lang), formatBytes(dataSent), localize({ en: "Bytes sent to the server.", tr: "Sunucuya gönderilen bayt." }, lang)),
    metricCard(tr("dataReceived", lang), formatBytes(dataReceived), localize({ en: "Bytes received from the server.", tr: "Sunucudan alınan bayt." }, lang)),
  ];

  return `<section class="panel" id="${lang}-throughput">
    <h2>${tr("throughput", lang)}</h2>
    <p class="muted">${tr("dataExp", lang)}</p>
    <div class="metric-grid">${rows.join("\n")}</div>
    <div class="callout-box"><p>${tr("peakRpsNa", lang)}</p></div>
  </section>`;
}

